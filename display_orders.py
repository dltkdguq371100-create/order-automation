def print_table(title, data):
    """바코드와 수량만 표 형태로 출력합니다."""
    print()
    print("=" * 40)
    print(f"  {title}")
    print("=" * 40)

    # 헤더
    print(f"{'NO':>3}  {'바코드':<15} {'수량':>4}")
    print("-" * 40)

    total_qty = 0
    for i, (barcode, qty) in enumerate(data, 1):
        total_qty += qty
        print(f"{i:>3}  {barcode:<15} {qty:>4}")

    print("-" * 40)
    print(f"  총 {len(data)}개 품목 / 총 수량: {total_qty}개")
    print("=" * 40)


